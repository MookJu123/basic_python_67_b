import math
x = min(5, 10, 15)  # หาค่าน้อยที่สุด
y = max(5, 10, 15)  # หาค่ามากที่สุด
print(x, y)

z = abs(-7.25)      # |-7.25| = 7.25
print(z)

v = pow(4, 3)       # 4 x 4 x 4 = 64
print(v)

pi = math.pi
print(pi)

s_root = math.sqrt(64)  # ถอดรูท
print(s_root)

x = math.ceil(1.3)      # การปัดทศนิยม'ขึ้นเสมอ'
y = math.floor(1.3)     # การปัดทศนิยม'ตามปกติ'
print(x)
print(y)




